import Foundation

class MyClass {
    func decreaseNumber(numberInt: Int) -> String {
        return String(numberInt-1)
    }
    
    func reset() -> String {
        return "0"
    }
    func increaseNumber(numberInt: Int) -> String {
        return String(numberInt+1)
    }
}


